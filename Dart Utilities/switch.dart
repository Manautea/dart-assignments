void main() {
  String fruit = 'orange';
  
  switch (fruit) {
    case 'apple':
      print('You chose an apple.');

      break;

    case 'banana':
      print('You chose a banana.');

      break;

      case 'mango':
      print('You chose a mango.');

      break;

      case 'orange':
      print('You chose an orange.');

      break;

    default:
      print('Unknown fruit.');
  }
}
